class ifladder
{
 public static void main(String args[])
{
int a = 15;
int b = 30;
int c = 25;

if(a>b && a>c)
{
System.out.println("a is greater than all");
}
else if(b>a && b>c)
{
System.out.println("b is greater than all");
}
else
{
System.out.println("c is greater than all");
}
}
}